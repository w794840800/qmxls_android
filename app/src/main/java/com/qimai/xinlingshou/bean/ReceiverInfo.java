public class ReceiverInfo<T> {

    private T data;
    private String dataModel;

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    @Override
    public String toString() {
        return "ReceiverInfo{" +
                "data=" + data +
                ", dataModel='" + dataModel + '\'' +
                '}';
    }

    public String getDataModel() {
        return dataModel;
    }

    public void setDataModel(String dataModel) {
        this.dataModel = dataModel;
    }
}
